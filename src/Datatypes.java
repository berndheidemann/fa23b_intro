public class Datatypes {
    public static void main(String[] args) {
        System.out.println("Hello World");

        // Datentypen
        // int --> Integer --> Ganzzahl --> 32 Bit
        // höchste Zahl --> 2 hoch 31, da ein Bit für das Vorzeichen reserviert
        int number=400000000;

        // double --> Dezimalzahl --> 64 Bit
        double d = 2314.123123123123123;
        System.out.println("double: " + d);

        // float --> Dezimalzahl --> 32 Bit
        float f = 12312.12224223123f;
        System.out.println("float: " + f);

        // short --> Ganzzahl --> 16 Bit
        short sh=32312;

        // long --> Ganzzahl --> 64 Bit
        long l=123213123123123L;

        // boolean --> Wahrheitswert --> 1 Bit
        boolean bool=false;

        // byte --> Ganzzahl  --> 8Bit
        byte by=127;

        // char --> ein Zeichen --> 16 Bit
        char c = 'z';

        // String
        String text="qpwidjpqwiojd";



    }
}